<p>&nbsp;</p>
<p><strong>Lowry does not have its own car park.&nbsp;</strong>Please use Quayside MediaCity for parking. It has 80 designated accessible parking bays available on every level of the car park.</p>
<p>During trading hours parking for Blue Badge holders is free of charge for the first four hours. To utilise the 4 free hours parking please visit the Customer Service Lounge on the first floor with your Blue Badge, leaving the clock part visible in your vehicle, you will receive a code to use at the pay station. Any additional time will be charged at the regular rate.</p>
<p><strong>Customer Service Lounge Trading Hours</strong></p>
<p>Monday 10am-6pm<br>Tuesday 10am-6pm<br>Wednesday 10am-6pm<br>Thursday 10am-8pm<br>Friday 10am-6pm<br>Saturday 10am-7pm<br>Sunday 11am-5pm</p>
<p>&nbsp;</p>
<h3>Enjoy our spaces with ease</h3>
<p>Here are some ways we&rsquo;re making it easier to use our building and find your way around:</p>
<ul>
<li>Accessible toilets throughout the building.</li>
<li>Step-free access to our Box Office, toilets, theatres and galleries.</li>
<li>Lift access to all floors.</li>
<li>Wheelchair spaces in all three theatres.</li>
<li>Please note our Ground Floor has a slight incline.</li>
</ul>
<p>&nbsp;</p>
<h3>Assistance dogs</h3>
<p>Assistance dogs are welcome in all of our theatres. If you&rsquo;d prefer them not to watch the show, we have a safe place for them to wait for you. We can provide water and our staff will check (and cuddle) your four-legged friend at regular intervals.</p>
<p>&nbsp;</p>
<h3>Wheelchair access</h3>
<p>Wheelchairs can be borrowed for your visit and can be reserved in advance by contacting &ndash;&nbsp;<a href="mailto:fohia@thelowry.com">fohia@thelowry.com</a></p>
<p>They are only allowed to be used in our building and the customer will need a companion to push them (our staff can&rsquo;t unfortunately).</p>
<p>There is no charge to borrow one, however we will take a name, email address and a phone number (personal details are removed when the chair is returned).</p>
<p>&nbsp;</p>
<h3>Galleries Relaxed Session</h3>
<p>This is a session for individuals and families with access requirements who would prefer to experience our Galleries in a calmer and less busy environment.</p>
<p><a class="btn btn-active" href="https://thelowry.com/whats-on/285//galleries-relaxed-session?_locale=en">Book tickets</a></p>
